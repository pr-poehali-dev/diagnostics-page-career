const questions = RAW_QUESTIONS.map((text, i) => ({ id: i + 1, text }));

const TOTAL = questions.length;

function computeResult(answers: Record<number, boolean>): TestResult {
  const scores: Record<string, number> = {};
  for (const prof of PROFESSIONS) {
    scores[prof.key] = prof.questions.filter((q) => answers[q] === true).length;
  }
  const top = PROFESSIONS.slice().sort((a, b) => scores[b.key] - scores[a.key])[0];
  return {
    type: top.key,
    label: top.label,
    scores,
  };
}

export default function TestPage({ onComplete }: TestPageProps) {
  const [answers, setAnswers] = useState<Record<number, boolean>>({});
  const [current, setCurrent] = useState(0);

  const answered = Object.keys(answers).length;
  const progress = (answered / TOTAL) * 100;
  const q = questions[current];

  const selectAnswer = (val: boolean) => {
    const newAnswers = { ...answers, [q.id]: val };
    setAnswers(newAnswers);

    if (current < TOTAL - 1) {
      setTimeout(() => setCurrent(current + 1), 220);
    } else {
      onComplete(computeResult(newAnswers));
    }
  };

  const goBack = () => setCurrent(Math.max(0, current - 1));
  const goNext = () => {
    if (answers[q.id] !== undefined && current < TOTAL - 1) setCurrent(current + 1);
  };

  return (
    <main className="min-h-screen pt-16 px-6 flex flex-col">
      {/* Progress */}
      <div className="max-w-2xl mx-auto w-full pt-10">
        <div className="flex items-center justify-between mb-3">
          <span className="text-xs" style={{ color: "var(--color-subtle)", fontFamily: "var(--font-body)" }}>
            Вопрос {Math.min(current + 1, TOTAL)} из {TOTAL}
          </span>
          <span className="text-xs" style={{ color: "var(--color-subtle)", fontFamily: "var(--font-body)" }}>
            {Math.round(progress)}%
          </span>
        </div>
        <div className="h-1 rounded-full overflow-hidden" style={{ background: "var(--color-line)" }}>
          <div
            className="h-full rounded-full transition-all duration-500"
            style={{ width: `${progress}%`, background: "var(--color-accent)" }}
          />
        </div>
      </div>

      {/* Question */}
      <div className="flex-1 flex items-center justify-center">
        <div className="max-w-2xl w-full mx-auto py-10">
          <div
            className="mb-2 text-xs uppercase tracking-widest"
            style={{ color: "var(--color-accent)", fontFamily: "var(--font-body)" }}
          >
            Любите ли вы? / Нравится ли вам? / Хотели бы вы?
          </div>

          <h2
            className="font-display text-2xl md:text-3xl font-semibold mb-10 leading-snug"
            style={{ color: "var(--color-text)" }}
          >
            {q.text}
          </h2>

          <div className="flex gap-4">
            {/* Да */}
            <button
              onClick={() => selectAnswer(true)}
              className="flex-1 py-5 rounded-2xl text-base font-semibold transition-all duration-200 hover:scale-[1.02] active:scale-95"
              style={{
                background: answers[q.id] === true ? "var(--color-accent)" : "var(--color-card)",
                color: answers[q.id] === true ? "#fff" : "var(--color-text)",
                border: `1.5px solid ${answers[q.id] === true ? "var(--color-accent)" : "var(--color-line)"}`,
                fontFamily: "var(--font-display)",
              }}
            >
              Да
            </button>
            {/* Нет */}
            <button
              onClick={() => selectAnswer(false)}
              className="flex-1 py-5 rounded-2xl text-base font-semibold transition-all duration-200 hover:scale-[1.02] active:scale-95"
              style={{
                background: answers[q.id] === false ? "var(--color-text)" : "var(--color-card)",
                color: answers[q.id] === false ? "var(--color-bg)" : "var(--color-text)",
                border: `1.5px solid ${answers[q.id] === false ? "var(--color-text)" : "var(--color-line)"}`,
                fontFamily: "var(--font-display)",
              }}
            >
              Нет
            </button>
          </div>

          <div className="flex items-center justify-between mt-8">
            <button
              onClick={goBack}
              disabled={current === 0}
              className="text-sm px-4 py-2 rounded-xl transition-all"
              style={{
                color: current === 0 ? "var(--color-subtle)" : "var(--color-muted)",
                fontFamily: "var(--font-body)",
                opacity: current === 0 ? 0.4 : 1,
              }}
            >
              ← Назад
            </button>
            {answers[q.id] !== undefined && current < TOTAL - 1 && (
              <button
                onClick={goNext}
                className="text-sm px-4 py-2 rounded-xl transition-all"
                style={{ color: "var(--color-muted)", fontFamily: "var(--font-body)" }}
              >
                Далее →
              </button>
            )}
            {answers[q.id] !== undefined && current === TOTAL - 1 && (
              <button
                onClick={() => onComplete(computeResult(answers))}
                className="text-sm px-6 py-2 rounded-xl font-semibold transition-all"
                style={{
                  background: "var(--color-accent)",
                  color: "#fff",
                  fontFamily: "var(--font-display)",
                }}
              >
                Завершить →
              </button>
            )}
          </div>
        </div>
      </div>
    </main>
  );
}
